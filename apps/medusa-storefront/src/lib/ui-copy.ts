const INTERPOLATION_PATTERN = /\{(\w+)\}/g

export const normalizeUiLocale = (locale: string | null | undefined): UiLocale => {
  const primary = locale?.split(/[-_]/)[0]?.toLowerCase()

  if (primary === "ar" || primary === "he") {
    return primary
  }

  return "en"
}

export const getUiCopy = (
  locale: string | null | undefined,
  key: UiCopyKey,
  params?: Record<string, string | number>
) => {
  const normalizedLocale = normalizeUiLocale(locale)
  const template =
    UI_COPY[normalizedLocale][key] ?? UI_COPY.en[key] ?? key

  if (!params) {
    return template
  }

  return template.replace(INTERPOLATION_PATTERN, (_, name: string) => {
    const value = params[name]
    return value === undefined || value === null ? "" : String(value)
  })
}

export type { UiCopyKey, UiLocale }